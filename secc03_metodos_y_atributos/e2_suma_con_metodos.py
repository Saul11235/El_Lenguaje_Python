# Hacer un programa que lea tres números 
# y los sume, pero utilizando el método __add__
#

print("SUMA MEDIANTE METODOS\n")

n1=float(input("ingrese 1er nro: "))
n2=float(input("ingrese 2do nro: "))
n3=float(input("ingrese 3er nro: "))

print("\nSuma Total:",n1.__add__(n2).__add__(n3))
